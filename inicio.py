letra = input("Digite uma letra do alfabeto: ")

letra = letra.lower()

if letra == 'a':
    print("Avião")
elif letra == 'b':
    print("Bola")
elif letra == 'c':
    print("Carro")
elif letra == 'd':
    print("Dado")
elif letra == 'e':
    print("Elefante")
elif letra == 'f':
    print("Foca")
elif letra == 'g':
    print("Gato")
elif letra == 'h':
    print("Hipopótamo")
else:
    print("Entrada inválida. Por favor, digite uma letra do alfabeto.")